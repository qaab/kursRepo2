public class NumPrinterToFive {
    public static void main(String[] args) {
        int i = 1;
        while (i <=5) {
            System.out.println("Liczba " + i);
            i++;
            // program drukuje na ekranie słowo liczba wraz z cyfrą, ze skokiem o jeden, dopóki nie wyrukuje liczbyu 5 (i nie będzie równe 5)
        }
    }
}
