public class MathsOperators {
    public static void main(String[] args) {
        int x = 10;
        int y = 4;

        int sum = x + y; // suma zmaiennych int zdefinowanych na początku
        int sub = x - y; // odejmowanie
        int multi = x * y; // mnożenie
        double div = x /y; // dzielenie liczb całkowitych - bez reszty
        int mod = x % y; // reszta z dzielenia - wynik modulo

        System.out.println(sum);
        System.out.println(sub);
        System.out.println(multi);
        System.out.println(div);
        System.out.println(mod);
    }
}
