public class UpandDown {
    public static void main(String[] args) {
        int num = 3;
        System.out.println(num++);
        System.out.println(num); // napierw wyświetla potem zwiększa
        System.out.println(++num); // najpierw zwieksza potem wyświetla
        System.out.println(num);
        System.out.println(--num);
        System.out.println(num--);
        System.out.println(num++);

    }
}
